#include <iostream>
#include <iomanip>
// Input/Output Manipulators（输入/输出操纵符）的缩写。

//如果你需要美化控制台输出（例如对齐表格、控制小数位数、填充特定字符），
//或者控制输入的格式，你就必须引入这个头文件。它主要用于对流（stream）进行带参数的格式化操作。

/****************************************************************************

1、控制输出宽度与对齐： std::setw

功能：设置下一个输出字段的最小宽度。
注意：setw 是一次性的。它只对紧随其后的那个输出有效，之后会恢复默认行为。
配合：默认是右对齐，如果需要左对齐，需配合 <iostream> 中的 std::left 使用。

*****************************************************************************/
void fun1()
{
	std::cout << std::setw(10) << "Name" << std::setw(5) << "Age" << std::endl;
	std::cout << std::setw(10) << "Alice" << std::setw(5) << 20 << std::endl;
}
// 输出：
//       Name  Age
//      Alice   20

/****************************************************************************

2、填充字符： std::setfill

功能：设置用于填充空白宽度的字符。
持久性：它是持久的，设置一次后，直到下次改变前一直有效。

*****************************************************************************/
void fun2()
{
	std::cout << std::setfill('*') << std::setw(10) << "Hi" << std::endl;
}
// 输出：********Hi (默认右对齐，左侧填充)

/****************************************************************************

3、控制浮点数精度： std::setprecision

功能：控制输出的有效数字或小数位数。
持久性：持久有效。
两种模式：
	默认模式：setprecision(n) 控制有效数字的总位数（整数部分+小数部分）。
	固定点模式 (std::fixed)：配合使用时，setprecision(n) 控制小数点后的位数。

*****************************************************************************/
void fun3()
{
	double pi = 3.1415926;

	// 模式1：总有效数字
	std::cout << std::setprecision(4) << pi << std::endl;
	// 输出：3.142 (四舍五入)

	// 模式2：固定小数位数 (常用)
	std::cout << std::fixed << std::setprecision(2) << pi << std::endl;
	// 输出：3.14
}

/****************************************************************************

4、进制转换： std::setbase

功能：设置整数的输出进制（8, 10, 16）。
参数：只能是 8, 10, 或 16。其他值会被重置为 10。

*****************************************************************************/
void fun4()
{
	int num = 255;
	std::cout << std::setbase(16) << num << std::endl; // 输出：ff
	std::cout << std::setbase(8) << num << std::endl;  // 输出：377
}
